import Head from 'next/head'

import { useNavTab } from '@/common/hook/useNavTab'
import { ProfileLoading } from '@/component/ui/Loading'

import { useQueryMyProfile } from './hook'
import { MyProfile } from './MyProfile'

export const MyProfilePage = () => {
  const { data: myProfile, status: myProfileStatus } = useQueryMyProfile()

  useNavTab('questions', 'my-profile')

  if (myProfileStatus == 'loading') return <ProfileLoading />

  return (
    <>
      <Head>
        <title>Recurrot - プロフィール</title>
        <meta name='description' content='Generated by create next app' />
        <meta name='viewport' content='width=device-width, initial-scale=1' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      {myProfile && <MyProfile myProfile={myProfile} />}
    </>
  )
}
